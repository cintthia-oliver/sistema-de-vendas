    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        //Botão Pagamento - Passar as informações de um Frame para outro
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        
        Cliente obj = new Cliente();
        ClienteDAO dao = new ClienteDAO();
        
        obj = dao.buscarCliente(nome);
        obj = dao.buscarClienteCPF(cpf);
        
        if(obj.getNome() != null && obj.getCpf() != null){
            FormPagamento pagamento = new FormPagamento();
            pagamento.txtTotal.setText(String.valueOf(total));
            pagamento.cliente = obj;
            pagamento.carrinho = carrinho;
            
            pagamento.setVisible(true);
        }
    }//GEN-LAST:event_btnPagarActionPerformed

