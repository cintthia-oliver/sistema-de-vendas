    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        //Método para Adicionar ao carrinho
        String nome = txtProduto.getText();
        
        Produto obj = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        
        obj = dao.buscarProdutoNome(nome);
        
        if(obj.getDescricao() != null){
            int quantidade = Integer.valueOf(txtQnt.getText());
            int estoque = Integer.valueOf(txtEstoque.getText());
            preco = Double.valueOf(txtPreco.getText());
            qtd = Integer.valueOf(txtQnt.getText());
            subtotal = preco * qtd;
            total += subtotal;
            
            if(estoque >= quantidade){
                txtTotal.setText(String.valueOf(total));
                
                carrinho = (DefaultTableModel) tbConsultaCarrinho.getModel();
                carrinho.addRow(new Object[]{
                    txtCodigo.getText(),
                    txtProduto.getText(),
                    txtQnt.getText(),
                    txtPreco.getText(),
                    subtotal
                });
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Quantidade indisponível no estoque!");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Faltam dados do produto a serem informados!");
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

